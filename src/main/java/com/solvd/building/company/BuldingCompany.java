package com.solvd.building.company;

public class BuldingCompany {
    private String companyName;
    private String companyAdress;
    private int numberOfEployee;
    private int costOfCompany;
    private int companyBudget;

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public String getCompanyAdress() {
        return companyAdress;
    }

    public void setCompanyAdress(String companyAdress) {
        this.companyAdress = companyAdress;
    }

    public int getNumberOfEployee() {
        return numberOfEployee;
    }

    public void setNumberOfEployee(int numberOfEployee) {
        this.numberOfEployee = numberOfEployee;
    }

    public int getCostOfCompany() {
        return costOfCompany;
    }

    public void setCostOfCompany(int costOfCompany) {
        this.costOfCompany = costOfCompany;
    }

    public int getCompanyBudget() {
        return companyBudget;
    }

    public void setCompanyBudget(int companyBudget) {
        this.companyBudget = companyBudget;
    }
}
